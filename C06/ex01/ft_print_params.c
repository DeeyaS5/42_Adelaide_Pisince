#include <unistd.h>

int	main(int argc, char **argv)
{
	int		r;
	int		c;

	r = 1;
	c = 0;
	while (r < argc)
	{
		while (argv[r][c])
		{
			write(1, &argv[r][c], 1);
			c++;
		}
		r++;
		write(1, "\n", 1);
		c = 0;
	}
	return (0);
}
